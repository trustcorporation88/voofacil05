function normalize(str: string): string {
  return str
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "");
}

export function searchLocalAirports(query: string): Airport[] {
  if (!query || query.length < 2) return [];
  const q = normalize(query);
  return POPULAR_AIRPORTS.filter((a) => {
    return (
      normalize(a.iataCode).includes(q) ||
      normalize(a.name).includes(q) ||
      normalize(a.cityName).includes(q) ||
      normalize(a.countryCode).includes(q) ||
      `${normalize(a.cityName)} ${normalize(a.iataCode)}`.includes(q) ||
      `${normalize(a.iataCode)} ${normalize(a.cityName)}`.includes(q)
    );
  });
}
